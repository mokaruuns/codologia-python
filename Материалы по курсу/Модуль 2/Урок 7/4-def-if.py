def wordToNumber(string):
    if string == "один":
        number = 1
    elif string == "два":
        number = 2
    elif string == "три":
        number = 3
    elif string == "четыре":
        number = 4
    elif string == "пять":
        number = 5
    elif string == "шесть":
        number = 6
    elif string == "семь":
        number = 7
    elif string == "восемь":
        number = 8
    elif string == "девять":
        number = 9
    elif string == "ноль":
        number = 0
    return number
