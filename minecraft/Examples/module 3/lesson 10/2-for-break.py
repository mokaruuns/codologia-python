# читаем число, введённое пользователем
n = int(input())
# счётчик пробегает n чисел
for i in range(n):
    # читаем новое число с экрана
    number = int(input())
    # если число отрицательное (меньше 0), то печатаем на экран, что такое есть
    if number < 0:
        print("есть отрицательное число!")
        break
